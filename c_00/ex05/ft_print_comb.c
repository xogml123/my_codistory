#include <unistd.h>

void	ft_print_comb(void)
{
	int		i[3];
	char	*enter;

	i[0] = 48;
	enter = ", ";
	while (i[0] < 56)
	{
		i[1] = i[0] + 1;
		while (i[1] < 57)
		{
			i[2] = i[1] + 1;
			while (i[2] < 58)
			{
				write(1, &i[0], 1);
				write(1, &i[1], 1);
				write(1, &i[2], 1);
				if (i[0] != 55 || i[1] != 56 || i[2] != 57)
					write(1, enter, 2);
				i[2]++;
			}
			i[1]++;
		}
		i[0]++;
	}
}
