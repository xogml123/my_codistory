int			ft_str_is_alpha(char *str)
{
	int		i;
	char	ch;

	i = 0;
	if (str[i] == '\0')
		return (1);
	while (1)
	{
		ch = str[i];
		if (ch == '\0')
			return (1);
		else if (!((ch >= 'a' && ch <= 'z') || (ch >= 'A' && ch <= 'Z')))
			return (0);
		i++;
	}
}
